package UTS;

public class FiqalSubString {
    public static void main(String[] args) {
        String alfabet = "ABCDEFGHIJKLMNOPQRSTUVWXYZ#-";

        System.out.println(alfabet);
        System.out.println("" + alfabet.substring(5,6));
        System.out.println("" + alfabet.substring(8,9));
        System.out.println("" + alfabet.substring(16,17));
        System.out.println("" + alfabet.substring(0,1));
        System.out.println("" + alfabet.substring(11,12));
    }
}
